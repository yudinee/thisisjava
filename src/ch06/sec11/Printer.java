package ch06.sec11;

public class Printer {
	

	// q.16
		void println (int aa) {
			System.out.println(aa);
		}
		
		void println (boolean aa) {
			System.out.println(aa);
		}
		
		void println (double aa) {
			System.out.println(aa);
		}
		
		void println (String aa) {
			System.out.println(aa);
		}
		
		void println (String aa, int a) {
			System.out.println(aa + a);
		}
		
		
		
		//q.17
		
		static void print (int aa) {
			System.out.println(aa);
		}
		
		static void print (boolean aa) {
			System.out.println(aa);
		}
		
		static void print (double aa) {
			System.out.println(aa);
		}
		
		static void print (String aa) {
			System.out.println(aa);
		}
		
		static void print (String aa, int a) {
			System.out.println(aa + a);
		}
		
		
}
